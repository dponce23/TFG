Tipos de cookies
<p>
    Tipos de cookies Según quien sea la entidad que gestione el dominio desde
    donde se envían las cookies y trate los datos que se obtengan se pueden
    distinguir dos tipos: cookies propias y cookies de terceros. Existe también
    una segunda clasificación según el plazo de tiempo que permanecen
    almacenadas en el navegador del cliente, pudiendo tratarse de cookies de
    sesión o cookies persistentes. Por último, existe otra clasificación con
    cinco tipos de cookies según la finalidad para la que se traten los datos
    obtenidos: cookies técnicas, cookies de personalización, cookies de
    análisis, cookies publicitarias y cookies de publicidad comportamental. Para
    más información a este respecto puedes consultar la Guía sobre el uso de las
    cookies de la Agencia Española de Protección de Datos.
</p>
Cookies utilizadas en el portal web
<p>
