    cookies que están siendo utilizadas en este portal así como su tipología y
    función: La página web administracion.gob.es utiliza Google Analytics, un
    servicio de analítica web desarrollada por Google, que permite la medición y
    análisis de la navegación en las páginas web. En su navegador podrá observar
    4 cookies de este servicio. Según la tipología anterior, se trata de cookies
    propias, de sesión y de análisis. Puede encontrar más información al
    respecto e inhabilitar el uso de estas cookies en:
    www.google.es/intl/es/analytics/privacyoverview.html. A través de la
    analítica web se obtiene información relativa al número de usuarios que
    acceden a la web, el número de páginas vistas, la frecuencia y repetición de
    las visitas, su duración, el navegador utilizado, el operador que presta el
    servicio, el idioma, el terminal que utiliza, o la ciudad a la que está
    asignada su dirección IP. Información que posibilita un mejor y más
    apropiado servicio por parte de este portal. Otras de las cookies que se
    utilizan son: Cookie de información del usuario USUARIO_SESION. Esta cookie
